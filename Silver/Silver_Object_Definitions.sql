-- Audit_Job_Log table
create or replace TABLE SILVER.FINANCE.AUDIT_JOB_LOG (
	JOB_ID VARCHAR(255),
	JOB_NAME VARCHAR(255),
	LAYER_NAME VARCHAR(255),
	SOURCE_OBJECT VARCHAR(255),
	TARGET_OBJECT VARCHAR(255),
	START_TIME TIMESTAMP_NTZ(9),
