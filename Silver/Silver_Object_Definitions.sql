-- ============================================================
-- TASKS to load data to SILVER tables from BRONZE streams
-- ============================================================
-- Task for CUSTOMERS table
CREATE OR REPLACE TASK SILVER.FINANCE.TASK_CUSTOMERS_SILVER_LOAD
WAREHOUSE = COMPUTE_WH
SCHEDULE = '1 MINUTE'
WHEN SYSTEM$STREAM_HAS_DATA('BRONZE.FINANCE.BRONZE_CUSTOMERS_STREAM')
AS
CALL SILVER.UTILS.SP_GENERIC_SILVER_LOAD
(
    'CUSTOMERS_BRONZE_TO_SILVER',
    'SILVER',
    'BRONZE.FINANCE.CUSTOMERS',
    'SILVER.FINANCE.CUSTOMERS',
    'SILVER.UTILS.SP_CUSTOMERS_SILVER_TRANSFORMATIONS',
    'kgirija@defteam.co'
);

ALTER TASK SILVER.FINANCE.TASK_CUSTOMERS_SILVER_LOAD RESUME;
